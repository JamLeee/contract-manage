        </p>
        <p style="line-height:21pt; margin:0pt; orphans:0;    text-indent:24pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >9.5 借款人应遵循贷款人与办理贷款业务相关的业务制度及操作惯例，包括但不限于配合贷款人对贷款使用情况和借款人收入情况的监督检查，及时提供贷款人要求的资料、信息，并保证所提供资料和信息是真实、完整、准确的。</span>
        </p>
        <p style="line-height:21pt; margin:0pt; orphans:0;    text-indent:24pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">9.6 借款人应在下列事项发生或可能发生之日起五日内书面通知贷款人：</span>
        </p>
        <p style="line-height:21pt; margin:0pt; orphans:0;    text-indent:24pt; widows:0">
