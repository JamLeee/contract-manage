            style="font-family:宋体; font-size:12pt"
          >（1）对借款人还款能力有或可能有重大不利影响的诉讼、仲裁、行政措施、财产保全措施、强制执行措施或其他重大不利事件；</span>
        </p>
        <p style="line-height:21pt; margin:0pt; orphans:0;    text-indent:24pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">（2）借款人或其家庭成员的工作、收入发生重大变化；</span>
        </p>
        <p style="line-height:21pt; margin:0pt; orphans:0;    text-indent:24pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">（3）借款人姓名/名称、住所、联系方式、经营范围及方式发生变更；</span>
        </p>
        <p style="line-height:21pt; margin:0pt; orphans:0;    text-indent:24pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">（4）商号及其他投资人居所、出资额、出资方式等发生变更；</span>
        </p>
        <p style="line-height:21pt; margin:0pt; orphans:0;    text-indent:24pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">（5）借款人出售、出租、转移或以其他方式处分个人或商号资产的全部或大部分。</span>
        </p>
        <p style="line-height:21pt; margin:0pt; orphans:0;    text-indent:24pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">9.7 借款人必须提供贷款人认可的有效担保方式，并与贷款人签订相关的担保合同。</span>
