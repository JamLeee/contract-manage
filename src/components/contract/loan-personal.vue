        <p style="line-height:21pt; margin:0pt; orphans:0;    text-indent:24pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">（3）借款人未履行第九条项下所列任何一项义务的实际发生，贷款人认为将影响其债权的安全；</span>
