        </p>
        <p
          style="line-height:21pt; margin:0pt 0.35pt 0pt 0pt; orphans:0;    text-indent:24.1pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt; font-weight:bold">第十条 违约</span>
        </p>
        <p
          style="line-height:21pt; margin:0pt 0.35pt 0pt 0pt; orphans:0;    text-indent:24pt; widows:0"
