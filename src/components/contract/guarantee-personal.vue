        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span
            style="font-family:宋体; font-size:12pt"
          >甲方同意乙方向中国人民银行个人信用信息基础数据库及信贷征信主管部门批准建立的个人信用数据库或有关单位、部门及个人查询甲方的信用状况，查询获得的信用报告限用于中国人民银行颁布的《个人信用信息基础数据库管理暂行办法》规定用途。甲方还同意乙方将甲方信用信息提供给中国人民银行个人信用信息基础数据库及信贷征信主管部门批准建立的个人信用数据库。甲方并同意，乙方为业务需要也可以合理使用并披露甲方信息。</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt">二、公告催收</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt">对甲方的违约情形，乙方有权向有关部门或单位予以通报，有权通过新闻媒体进行公告催收。</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt">三、乙方记录的证据效力</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span
            style="font-family:宋体; font-size:12pt"
          >除非有可靠、确定的相反证据，乙方有关本金、利息、费用和还款记录等内容的内部账务记载，乙方制作或保留的债务人办理提款、还款、付利息等业务过程中发生的单据、凭证及乙方催收贷款的记录、凭证，均构成有效证明主合同项下债权关系的确定证据。甲方不能仅因为上述记录、记载、单据、凭证由乙方单方制作或保留，而提出异议。</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt">四、</span>
          <span style="font-family:宋体; font-size:12pt">权利保留</span>
