          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt">第三条 保证期间</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span
            style="font-family:宋体; font-size:12pt"
          >一、本合同项下的保证期间按乙方为债务人办理的单笔授信业务分别计算，即自单笔授信业务的主合同签订之日起至债务人在该主合同项下的债务履行期限届满日后</span>
          <span style="font-family:宋体; font-size:12pt">两年止。</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span
            style="font-family:宋体; font-size:12pt"
          >二、乙方与债务人就主合同项下债务履行期限达成展期协议的,保证期间至展期协议重新约定的债务履行期限届满之日后两年止。展期无需经保证人同意，保证人仍需承担连带保证责任。</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span
            style="font-family:宋体; font-size:12pt"
          >三、若发生法律法规规定或主合同约定的事项，乙方宣布债务提前到期的，保证期间至债务提前到期之日后两年止。</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt"></span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt">第四条 保证合同的独立性</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span
            style="font-family:宋体; font-size:12pt"
          >本合同的效力独立于主合同，主合同不成立、不生效、无效、部分无效或被撤销、被解除并不影响本合同的效力。如主合同被确认为不成立、不生效、无效、部分无效或被撤销、被解除，则甲方对于债务人因返还财产或赔偿损失而形成的债务也承担连带责任。</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt"></span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt">第五条 主合同变更</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span
            style="font-family:宋体; font-size:12pt"
          >一、甲方同意，乙方与债务人签订主合同或对主合同进行任何变更（包括但不限于延长债务履行期限或增加债权本金金额），均无需通知甲方，甲方仍应在本合同约定的最高额以及担保范围内承担担保责任。</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt">二、甲方的保证责任不因出现下列任一情况而减免：</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt">（一）乙方或债务人发生改制、合并、兼并、分立、增减资本、合资、联营、更名等情形；</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt">（二）乙方委托第三方履行其在主合同项下的义务。</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt">三、主合同项下债权转让的，本合同项下的保证随之转让。</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span
            style="font-family:宋体; font-size:12pt"
          >四、主合同项下债权或债务的转移行为未生效、无效、被撤销、被解除，甲方仍按照本合同对乙方承担连带保证责任。</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt"></span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt">第六条 保证责任</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span
            style="font-family:宋体; font-size:12pt"
          >一、如果主合同项下债务到期或者乙方根据主合同的约定或法律规定宣布债务提前到期，债务人未按时足额履行，或者债务人违反主合同的其他约定，甲方应在保证范围内承担保证责任。</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt">
            如果甲方未在乙方要求的期限内全部支付应付款项，应自逾期之日起至甲方向乙方支付全部应付款项之日止，根据迟延付款金额按每日万分之
            <input
              style="width: 50px"
              v-model="info.a19"
              type="text"
            >
            的标准向乙方支付违约金。在此情形下，甲方承担的保证责任与上述违约金之和不以本合同约定的最高责任限额为限。
          </span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span
            style="font-family:宋体; font-size:12pt"
          >二、无论乙方对主合同项下的债权是否拥有其他担保（包括但不限于保证、抵押、质押、保函、备用信用证等担保方式），不论上述其他担保何时成立、是否有效、乙方是否向其他担保人提出权利主张，也不论是否有第三方同意承担主合同项下的全部或部分债务，也不论其他担保是否由债务人自己所提供，甲方在本合同项下的保证责任均不因此减免，乙方均可直接要求甲方依照本合同约定在其保证范围内承担保证责任，甲方将不提出任何异议。</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span
            style="font-family:宋体; font-size:12pt"
          >三、如果本合同约定的保证责任最高限额低于主合同项下实际发生的债权余额，在甲方承担保证责任后主合同项下的债权仍未获完全清偿，则甲方承诺，其向债务人或其他担保人主张（包括预先行使）代位权或追偿权，不应使乙方利益受到任何损害，并同意主合同项下债务的清偿优先于甲方代位权或追偿权的实现。</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt">具体而言，在乙方债权未被全部清偿前：</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span
            style="font-family:宋体; font-size:12pt"
          >（一）甲方同意不向债务人或其他担保人主张代位权或追偿权；如因任何原因，甲方实现了上述权利，则应将所获款项优先用于清偿乙方尚未获偿的债权；</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span
            style="font-family:宋体; font-size:12pt"
          >（二）主合同项下的债务如有物的担保，甲方同意不以行使代位权为由或任何其他原因对该担保物或其处分后所得价款提出权利主张，上述担保物及所得价款应优先用于清偿乙方尚未获偿的债权；</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span
            style="font-family:宋体; font-size:12pt"
          >（三）若债务人或其他担保人为甲方提供了反担保，则甲方基于上述反担保而获得的款项应优先用于清偿乙方尚未获偿的债权。</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span
            style="font-family:宋体; font-size:12pt"
          >四、甲方已充分认识到利率风险。如果乙方根据主合同的约定或者国家的利率政策变化而调整利率水平、计息或结息方式，导致债务人应偿还的利息、罚息、复利增加的，对增加部分，甲方也承担连带保证责任。</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt"></span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt">第七条 甲方的其他义务</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt">一、甲方应对债务人借款使用情况（包括用途）进行监督。</span>
