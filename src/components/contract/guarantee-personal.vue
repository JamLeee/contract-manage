            style="font-family:宋体; font-size:12pt"
          >如果乙方不行使或延缓行使主合同项下的任何权利或未用尽主合同项下的任何救济，甲方在本合同项下的保证责任并不因此减免，但是乙方若减免主合同项下债务，甲方在本合同项下的保证责任相应减免。</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt">五、债务人解散或破产</span>
