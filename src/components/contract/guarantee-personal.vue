        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt">二、</span>
          <span style="font-family:宋体; font-size:12pt">甲方应接受乙方对其财务状况的监督和检查，</span>
