        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span
            style="font-family:宋体; font-size:12pt"
          >四、甲方作为控股股东或实际控制人的企业发生合并、分立、股权变动、增减资本、合资、联营等情形，应当在发生上述情形后</span>
          <input style="width: 150px" v-model="info.a20" type="text">
          <span style="font-family:宋体; font-size:12pt">个工作日内书面通知乙方，并附相关材料。</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt">第八条 其他条款</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt">一、甲方信息的使用</span>
