        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span
            style="font-family:宋体; font-size:12pt"
          >甲方知道债务人进入解散或破产程序后，应当立即通知乙方申报债权，同时自己应及时参加解散或破产程序，预先行使追偿权。甲方知道或者应当知道债务人进入解散或破产程序，但未能及时预先行使追偿权的，其损失由甲方自行承担。</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span
            style="font-family:宋体; font-size:12pt"
          >在债务人破产程序中，如果乙方与债务人达成和解协议，或者同意重整计划，本合同项下乙方的权利不因和解协议或重整计划而受到损害，甲方的保证责任不予以减免。甲方不得以和解协议、重整计划规定的条件对抗乙方的权利主张。乙方在和解协议、重整计划中对债务人作出让步而未能获得清偿的债权部分，仍有权要求甲方继续予以清偿。</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span
            style="font-family:宋体; font-size:12pt"
          >六、甲方的通讯地址或联系方式如发生变动，应立即书面通知乙方，因未及时通知而造成的损失由甲方自行承担。</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt">七、 争议解决方式</span>
        </p>
        <p
          style="font-size:12pt; line-height:150%; margin:0pt; orphans:0; text-indent:23.6pt; widows:0"
