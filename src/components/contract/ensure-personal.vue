        </p>
        <p
          style="line-height:20pt; margin:0pt; orphans:0; text-align:justify; text-indent:20pt; widows:0"
        >
          <span
            style="font-family:宋体; font-size:12pt"
          >6.2 无论乙方对主合同项下的债权是否拥有其他担保（包括但不限于保证、抵押、质押、保函、备用信用证等担保方式），不论上述其他担保何时成立、是否有效、乙方是否向其他担保人提出权利主张，也不论是否有第三方同意承担主合同项下的全部或部分债务，也不论其他担保是否由债务人自己所提供，甲方在本合同项下的保证责任均不因此减免，乙方均可直接要求甲方依照本合同约定在其保证范围内承担保证责任，甲方将不提出任何异议。</span>
