          style="line-height:20pt; margin:0pt; orphans:0; text-align:justify; text-indent:20pt; widows:0"
        >
          <span
            style="font-family:宋体; font-size:12pt"
          >6.4 如果甲方只为主合同项下的部分债务提供保证，在其承担保证责任后主合同项下的债务仍未获完全清偿，则甲方承诺，其向债务人或其他担保人主张（包括预先行使）代位权或追偿权，不应使乙方利益受到任何损害，并同意主合同项</span>
          <span style="font-family:宋体; font-size:12pt">下债务的清偿优先于甲方代位权或追偿权的实现。</span>
