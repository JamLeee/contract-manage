          >金、利息（包括复利和罚息）、违约金、赔偿金、债务人应向乙方支付的其他款项（包括但不限于乙方垫付的有关手续费、电讯费、杂费、信用证项下受益人拒绝承担的有关银行费用等）、乙方实现债权与担保权利而发生的费用（包括但不限于诉讼费、仲裁费、财产保全费、差旅费、执行费、评估费、拍卖费、公证费、送达费、公告费、律师费等）。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt; font-weight:normal"
          >2.3 主合同项下的贷款、垫款、利息、费用或乙方的任何其他债权的实际形成时间即使超出债权确定期间，仍然属于本最高额抵押的担保范围。主合同项下债务履行期限届满日不受债权确定期间届满日的限制。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt; font-weight:bold">第三条 抵押财产的登记</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">3.1</span>
          <span style="font-family:宋体; font-size:12pt">双方应于本合同签订后</span>
          <input style="width: 50px" v-model="info.a18" type="text">
          <span style="font-family:宋体; font-size:12pt; text-decoration:underline"></span>
          <span
            style="font-family:宋体; font-size:12pt"
          >个工作日内到相应的登记部门办理抵押登记手续。甲方应于抵押登记完成之日将抵押财产的他项权利证书、抵押登记文件正本原件及其他</span>
          <span style="font-family:宋体; font-size:12pt">权利证书交乙方持有。抵押登记办理证书交付于乙方持有后方发放贷款。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt; font-weight:bold">第四条 主</span>
          <span style="font-family:宋体; font-size:12pt; font-weight:bold">合同变更</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">4.1</span>
          <span
            style="font-family:宋体; font-size:12pt"
          >甲方同意，乙方与债务人签订主合同或对主合同进行任何变更（包括但不限于延长债务履行期限或增加债权本金金额），均无需通知甲方，甲方仍应在本合同约定的最高额以及担保范围内承担担保责任。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">4.2 当事人变化</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">甲方的担保责任不因出现下列任一情况而减免：</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">（1）乙方或债务人发生改制、合并、兼并、分立、增减资本、合资、联营、更名等情形；</span>
          <span style="font-family:宋体; font-size:12pt"></span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">（2）乙方委托第三方履行其在主合同项下的义务；</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >4.3 主合同项下债权转移给第三人的，本合同项下的担保随之转让，甲方应协助乙方及该第三人办理法律所要求的抵押变更登记手续。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt; font-weight:normal"
          >4.4 主合同项下债权或债务的转移行为未生效、无效、被撤销、被解除，甲方仍按照本合同对乙方承担担保责任。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt; font-weight:bold">第五条 抵押财产的占有与保管</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >5.1 甲方应妥善地对抵押财产进行占有、保管和维修保养，合理使用抵押财产，维持抵押财产完好，按时缴纳与抵押财产相关的各项税费。乙方有权对抵押财产进行检查，并可要求甲方将抵押财产权利凭证的正本交乙方保管。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >5.2 甲方委托或同意第三方占有、保管、使用抵押财产的，应当告知该第三方乙方抵押权的存在，并要求其保持抵押财产的完好、接受乙方对抵押财产的检查以及不妨碍乙方实现抵押权。甲方不因此免除前款中的义务，同时应对该第三方的行为承担责任。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt; font-weight:normal"
          >5.3 抵押财产造成人身或财产损害的，应由甲方自行承担赔偿责任。如果乙方因此遭到索赔而承担了责任，或为甲方垫付了赔偿金，则乙方有权向甲方追偿。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt; font-weight:bold">第六条 抵押财产的保险</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >6.1 除非双方另有约定，甲方应根据有关法律及乙方指定的险种、保险期限、投保金额办理抵押财产的保险。保险人应当具备法定资质和良好信誉。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >6.2 保险单内容应当符合乙方的要求，不得附有损害乙方权益的限制性条件。保险单上应特别注明：乙方为保险赔偿金的优先受偿人（第一受益人）；变更保险单应经过乙方书面同意；一旦发生保险事故，保险人应将保险赔偿金直接划付至乙方指定的账户。抵押财产已保险的但保险单未注明上述内容的，应对保险单作出相应的批注或变更。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >6.3 甲方应确保保险连续有效，不得以任何理由致使保险中断、撤销、失效，或者致使保险人减免赔偿责任，或者不经乙方同意变更保险单。如果保险期间届满时，甲方担保的债权未全部受偿的，甲方应当续保，对保险期间作相应延长。</span>
        </p>
        <p
          style="line-height:25pt; margin:0pt 0pt 0pt 22.8pt; orphans:0; padding-left:15pt;  text-indent:-15pt; widows:0"
        >
          <span style="font-family:宋体; font-size:12pt">6.4 甲方应于本合同订立之日（抵押财产续保的，则为续保完成之日）起</span>
          <input style="width: 50px" v-model="info.a19" type="text">
          <span style="font-family:宋体; font-size:12pt; text-decoration:underline"></span>
          <span style="font-family:宋体; font-size:12pt">个</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >工作日内将抵押财产的保险单正本交付乙方，并且在乙方预留有关保险索赔或保险权益转让所必需的文件。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">6.5 就抵押财产取得的保险赔偿金，乙方有权选择下列方法进行处理，甲方应协助办理有关手续：</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">（1）经乙方同意，用于修复抵押财产，以恢复抵押财产价值；</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">（2）清偿或提前清偿主合同项下债务本息及相关费用；</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">（3）为主合同项下债务提供质押担保；</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt; font-weight:normal"
          >（4）甲方提供符合乙方要求的新的担保后，由甲方自由处分。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt; font-weight:bold">第七条 对甲方处分抵押财产的限制</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >7.1 未经乙方书面同意，甲方不得以任何方式处分抵押财产，包括但不限于放弃、出租（包括原租赁合同到期后续租）、赠予、转让、出资、</span>
          <span style="font-family:宋体; font-size:12pt">重复担保、迁移、改为公益用途、与其他物添附或改建、分割。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >甲方未经乙方同意进行的上述行为损害乙方债权的，乙方有权要求甲方撤销、终止以上行为或要求甲方提供其他相应的担保。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt; font-weight:normal"
          >7.2 经乙方书面同意，甲方处分抵押财产而获得价款或其他款项应当存入乙方指定的账户。乙方有权选择本合同第六条第6.5款第（2）至（4）项约定的任一方法对上述款项进行处理，甲方应协助办理有关手续。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt; font-weight:bold">第八条 第三方妨碍</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >8.1 如果抵押财产被国家征收、征用、拆除、没收、无偿收回，或者抵押财产被第三方查封、冻结、扣押、监管、留置、拍卖、强行占有、毁损或者进行其他处置，甲方应当立即通知乙方，并及时采取制止、排除或补救措施，防止损失扩大；若乙方提出要求，甲方应提供符合乙方要求的新担保。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt; font-weight:normal"
          >8.2 抵押财产发生前款情形后的剩余部分仍作为乙方债权的抵押担保。甲方因上述原因取得的赔偿金或补偿金，应存入乙方指定的账户。乙方有权选择本合同第六条第6.5款第（1）至（4）项约定的任一方法对上述款项进行处理，甲方应协助办理有关手续。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt; font-weight:bold">第九条 抵押权实现</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >9.1 债务人不履行主合同项下到期债务或不履行被宣布提前到期的债务，或违反主合同的其他约定，乙方有权处分抵押财产。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >9.2 本合同《抵押财产清单》记载的或双方另行约定的抵押财产价值（下称“暂定价值”），无论是否记载于登记机关的登记簿，均不表明抵押财产的最终价值，其最终价值为乙方处分抵押财产所得价款在扣除各项税费后的净额。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >若以抵押财产抵偿乙方债权的，上述暂定价值并不作为抵押财产抵偿乙方债权的依据，届时抵押财产的价值应由甲乙双方协商一致或依法公平评估确定。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >9.3 乙方处分抵押财产所得价款，在支付变卖或拍卖过程中的费用（包括但不限于保管费、评估费、拍卖费、过户费、税费、国有土地使用权出让金等）后，优先用于清偿主合同项下的债务，剩余价款退还甲方。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >9.4 甲方与债务人为同一人的，乙方可以对甲方的抵押财产之外的财产申请强制执行，且不以放弃抵押权或必须先处分抵押财产为前提条件。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">9.5 甲方不得以任何方式（包括作为或不作为）妨碍乙方实现抵押权。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >9.6 无论乙方对主合同项下的债权是否拥有其他担保（包括但不限于保证、抵押、质押、保函、备用信用证等担保方式），不论上述其他担保何时成立、是否有效、乙方是否向其他担保人提出权利主张，也不论是否有第三方同意承担主合同项下的全部或部分债务，也不论其他担保是否由债务人自己所提供，甲方在本合同项下的担保责任均不因此减免，乙方均可直接要求甲方依照本合同约定在其担保范围内承担担保责任，甲方将不提出任何异议。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">9.7</span>
          <span
            style="font-family:宋体; font-size:12pt"
          >如果本合同约定的担保责任最高限额低于主合同项下实际发生的债权余额，在甲方承担担保责任后主合同项下的债权仍未获完全清偿，则甲方承诺，其向债务人或其他担保人主张（包括预先行使）代位权或追偿权，不应使乙方利益受到任何损害，并同意主合同项下债务的清偿优先于甲方代位权或追偿权的实现。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">具体而言，在乙方债权未被全部清偿前，</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >（1）甲方同意不向债务人或其他担保人主张代位权或追偿权；如因任何原因，甲方实现了上述权利，则应将所获款项优先用于清偿乙方尚未获偿的债权；</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >（2）主合同项下的债务如有物的担保，甲方同意不以行使代位权为由或任何其他原因对该担保物或其处分后所得价款提出权利主张，上述担保物及所得价款应优先用于清偿乙方尚未获偿的债权；</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >（3）若债务人或其他担保人为甲方提供了反担保，则甲方基于上述反担保而获得的款项应优先用于清偿乙方尚未获偿的债权。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >9.8 如果主合同不成立、不生效、无效、部分无效或被撤销、被解除，且甲方与债务人不是同一人，则甲方在本合同约定的担保范围内对债务人因返还财产或赔偿损失而形成的债务与债务人承担连带责任。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >9.9 甲方已充分认识到利率风险。如果乙方根据主合同的约定或者国家的利率政策变化而调整利率水平、计息或结息方式，导致债务人应偿还的利息、罚息、复利增加的，对增加部分，甲方也承担担保责任。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt; font-weight:bold">第十条 违约责任</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">10.1 甲方违约责任</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >（1）甲方违反本合同的任一约定或陈述与保证的事项存在任何虚假、错误、遗漏，乙方有权采取以下一项或多项措施：</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">①要求甲方限期纠正违约行为；</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">②要求甲方提供新的担保；</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">③要求甲方赔偿损失；</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">④处分抵押财产；</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">⑤法律许可的其他救济措施。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >（2）乙方有权选择第六条第6.5款第（2）至（4）项约定的任一方法对处分抵押财产所得价款进行处理，甲方应协助办理有关手续。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >（3）如果因甲方原因导致抵押权未有效设立，或者导致抵押财产价值减少，或者导致乙方未及时或未充分实现抵押权，且甲方与债务人不是同一人，乙方有权要求甲方在本合同约定的担保范围内对担保的债务与债务人承担连带责任。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">10.2 乙方的违约责任：</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >如果乙方因过错遗失甲方交付的抵押财产权利证书；或者主合同项下债务清偿完毕后，乙方不及时返还抵押财产权利证书或在甲方提出申请后不依法协助办理抵押登记注销手续，甲方有权采取以下一项或多项措施：</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">（1）要求乙方承担抵押财产权利证书的补办费用；</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt; font-weight:normal"
          >（2）要求乙方限期返还抵押财产的权利证书，或协助甲方注销抵押财产登记。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt; font-weight:bold">第十一条 其他条款</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">11.1 费用承担</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >与本合同及本合同项下抵押财产有关的一切费用（包括但不限于占有、管理、处置、登记、公证、保险、运输、仓储、保管、估价、维修、保养、拍卖、过户等费用），均由甲方承担，双方另有约定的除外。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">11.2 应付款项的划收</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >对于甲方在本合同项下的全部应付款项，乙方有权从甲方的银行账户中划收人民币或其他币种的相应款项，且无须提前通知甲方。需要办理结售汇或外汇买卖手续的，甲方有义务协助乙方办理，汇率风险由甲方承担。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">11.3 甲方信息的使用</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >甲方同意乙方向中国人民银行及信贷征信主管部门批准建立的信用数据库或有关单位、部门查询甲方的信用状况，并同意乙方将甲方信息提供给中国人民银行及信贷征信主管部门批准建立的信用数据库。甲方并同意，乙方为业务需要也可以合理使用并披露甲方信息。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">11.4 公告催收</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">对甲方发生违约情形，乙方有权向有关部门或单位予以通报，有权通过新闻媒体进行公告催收。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">11.5 乙方记录的证据效力</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >除非有可靠、确定的相反证据，乙方有关本金、利息、费用和还款记录等内容的内部账务记载，乙方制作或保留的债务人办理提款、还款、付利息等业务过程中发生的单据、凭证及乙方催收贷款的记录、凭证，均构成有效证明主合同项下债权关系的确定证据。甲方不能仅因为上述记录、记载、单据、凭证由乙方单方制作或保留，而提出异议。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">11.6 权利保留</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">乙方在本合同项</span>
          <span
            style="font-family:宋体; font-size:12pt"
          >下的权利并不影响和排除其根据法律、法规和其它合同所享有的任何权利。任何对违约或延误行为施以宽容、宽限、优惠或延缓等行为，均不能视为对本合同项下权利、权益的放弃或对任何违反本合同行为的许可或认可，</span>
          <span style="font-family:宋体; font-size:12pt">也不影</span>
          <span
            style="font-family:宋体; font-size:12pt"
          >响、阻止和妨碍对该权利的继续行使或对其任何其它权利的行使，也不因此导致乙方对甲方承担义务和责任。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >即使乙方不行使或延缓行使主合同项下的任何权利或未用尽主合同项下的任何救济，甲方在本合同项下的担保责任并不因此减免，但是乙方若减免主合同项下债务，甲方在本合同项下的担保责任相应减免。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >11.7 甲方如发生分立、解散、进入破产程序、被撤销、被注销工商登记、被吊销营业执照，抵押财产因自然因素或者因第三人行为而毁损、灭失、被侵害、脱离甲方控制，抵押财产权属发生争议或权利证书（明）被注销，均应</span>
          <span
            style="font-family:宋体; font-size:12pt"
          >立即通知乙方。如出现上述情形，甲方未履行或怠于履行通知义务，导致乙方受损，甲方应赔偿乙方所受损失。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">11.8 债务人解散或破产</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >甲方知道债务人进入解散或破产程序后，应当立即通知乙方申报债权，同时自己应及时参加解散或破产程序，预先行使追偿权。甲方知道或者应当知道债务人进入解散或破产程序，但未能及时预先行使追偿权的，其损失由甲方自行承担。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >尽管有本条第11.6款第二项的约定，在债务人破产程序中，如果乙方与债务人达成和解协议，或者同意重整计划，本合同项下乙方的权利不因和解协议或重整计划而受到损害，甲方的担保责任不予以减免。甲方不得以和解协议、重整计划规定的条件对抗乙方的权利主张。乙方在和解协议、重整计划中对债务人作出让步而未能获得清偿的债权部分，仍有权要求甲方承担担保责任。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span style="font-family:宋体; font-size:12pt">11.9 甲方解散或破产</span>
