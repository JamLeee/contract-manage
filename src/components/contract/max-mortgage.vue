        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >甲方发生解散或破产的，即使乙方在主合同项下债权尚未到期，乙方也有权参加甲方清算或破产程序，申报权利。</span>
        </p>
        <p style="line-height:25pt; margin:0pt; orphans:0;  text-indent:20pt; widows:0">
          <span
            style="font-family:宋体; font-size:12pt"
          >11.10 甲方的通讯地址或联系方式如发生变动，应立即书面通知乙方，因未及时通知而造成的损失由甲方自行承担。</span>
        </p>
